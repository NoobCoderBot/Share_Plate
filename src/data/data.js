export const getExpiryInfo = (days) => {
  if (days <= 1) return { cls: 'tag-urgent', label: 'Expires today', color: 'red' };
  if (days <= 3) return { cls: 'tag-soon', label: `${days} days left`, color: 'amber' };
  return { cls: 'tag-good', label: `Good for ${days} days`, color: 'green' };
};

